package com.ec.model.dto;

public class CareerListDTO {
	private int expert_idx;
	private String career_name;
	private String start_time;
	private String end_time;
	
	public int getExpert_idx() {
		return expert_idx;
	}
	public void setExpert_idx(int expert_idx) {
		this.expert_idx = expert_idx;
	}
	public String getCareer_name() {
		return career_name;
	}
	public void setCareer_name(String career_name) {
		this.career_name = career_name;
	}
	public String getStart_time() {
		return start_time;
	}
	public void setStart_time(String start_time) {
		this.start_time = start_time;
	}
	public String getEnd_time() {
		return end_time;
	}
	public void setEnd_time(String end_time) {
		this.end_time = end_time;
	}
	
	
}
